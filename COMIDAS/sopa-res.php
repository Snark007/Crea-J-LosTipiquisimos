                <h1>Preparación:</h1>
                <ol>
                    <li>
                        En una olla de preferencia grande colocar el  agua y dejar hervir con el hueso de res  a fuego medio cuando esté hirviendo dejar medio destapado durante 20-25 minutos.
                        </li><br>
        
                    <li>Cuando esté hirviendo agregarles las hierbas aromáticas como el cilantro, apio, perejil junto con el tomate, cebolla y los dientes de ajo, tapamos y dejamos cocinar durante 20 minutos, recuerda verificar cada cierto tiempo el nivel de agua y agregar más si fuese necesario.</li><br>
        
                    <li>Mientras se cocina el hueso, vamos a partir las verduras en porciones pequeñas 
                        para facilitar la cocción, estas se irán incorporando conforme al nivel de cocción que necesite más tiempo.
                        </li><br>
        
                    <li>Recuerda que la yuca y el elote son los que toman más tiempo para su cocción, una vez estén suaves se le agrega el resto de verduras.</li><br>
    
                    <li>Revuelve regularmente para evitar que se adhiera al fondo.</li><br>
    
                    <li>Ajusta el agua cuando sea necesario y cuando el arroz esté muy blando agrega la leche y la mitad del azúcar.</li><br>
    
