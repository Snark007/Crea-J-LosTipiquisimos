                <h1>Preparación:</h1>
                <ol>
                    <li>Corta todas las piñitas del gajo, quilates la puntita con un cuchillo y luego las pones a hervir en agua por diez minutos. Retirarás del fuego, déjalas enfriar y luego a cada una le
