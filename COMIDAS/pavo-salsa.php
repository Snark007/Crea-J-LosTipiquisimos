            <div class="ingredientes-pasos">
                <h1>Preparación:</h1>
                <ol>
                    <li>Empezamos por lavar muy bien los trozos de pavo para salpimentarlos al gusto. Luego, picamos los dientes de ajo en rodajas y los vamos colocando de forma aleatoria dentro de los trozos que tengamos de pavo.</li><br>
    
                <li>Para terminar de sazonar el pavo, trituramos la pastilla de caldo hasta hacerla polvo y espolvoreamos con esto todos los trozos de carne. Reservar.</li><br>
    
                <li>Por otro lado, ponemos aceite en una cazuela grande, con medio dedo será suficiente, y freímos las hojas de laurel junto con la cebolla en cascos o trozos grandes. Cuando ya estén listas, retiramos ambos ingredientes y reservamos aparte.</li><br>
    
                <li>Continuamos la cocción en ese mismo aceite, pero ahora añadimos los trozos de pavo. Cocinamos hasta ver que se doran por todos lados y entonces los pasamos a otra cazuela nueva, que esté fría, junto con el aceite de la cocción.</li><br>
           
                <li>-Añadimos el vino en esta nueva cazuela junto con un poco de agua, la canela, los clavos y la pimienta en grano.
                    Continuamos con la preparación de nuestro pavo en salsa, dejando la llama a fuego lento.
                    </li><br>
               
                <li>Esperamos hasta que el pavo esté tierno y la salsa la podemos espesar al gusto con un poco de maicena o harina.
                    Recuerda que para usar cualquiera de las dos opciones deberás diluirla aparte antes de integrarla en la preparación.
                    </li><br>

                <li>Cuando el pavo en salsa esté listo y justo antes de servir, machacamos en un mortero el resto de los ajos fritos, los trozos de cebolla, las almendras y la molleja del pavo (previamente frita).</li><br>
                       
                <li>Este plato de pavo en salsa tiene un sabor espectacular y si no te gusta el pavo, puedes seguir el mismo procedimiento con pollo, ¡quedará buenísimo también.</li>
                </ol>
