                <h1>Ingredientes:</h1>
                <p>
                <ul type="square">
                    <li> 1 pavo troceado (4 k)</li>
                    <li>  1 cabeza de Ajo</li>
                    <li>  1 pizca de Sal y Pimienta molida</li>
                    <li>  1 pastilla de Caldo de ave</li>
                    <li> 1 chorro de Aceite de oliva</li>
                    <li>  1 cebolla</li>
                    <li>  1 vaso pequeño de Vino blanco</li>
                    <li>  1 trozo de Canela</li>
                    <li>  3 gramos de Clavos de olor</li>
                    <li> 10 granos de Pimienta</li>
                    <li> 1 puñado de Almendras</li>
                    <li>  Agua</li>
                    <li>  3 hojas de Laurel</li>

                </ul>
                </p>
